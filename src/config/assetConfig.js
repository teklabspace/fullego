// Helper function to get form fields for a category
export const getFormFieldsForCategory = categoryId => {
  const categoryGroup = getCategoryGroup(categoryId);
  return categoryGroupConfig[categoryGroup]?.formFields || [];
};

// Helper function to get card fields for a category
export const getCardFieldsForCategory = categoryId => {
  const categoryGroup = getCategoryGroup(categoryId);
  return categoryGroupConfig[categoryGroup]?.cardFields || [];
};

// Get categories by group
export const getCategoriesByGroup = groupName => {
  return allCategories.filter(cat => cat.categoryGroup === groupName);
};

// Get all unique category groups
export const getCategoryGroups = () => {
  return [...new Set(allCategories.map(cat => cat.categoryGroup))];
};

